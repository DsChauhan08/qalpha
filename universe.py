def is_sp500(ticker: str) -> bool:
    """Check if a ticker is in the S&P 500."""
    return ticker in _SP500_SET


def is_sp400(ticker: str) -> bool:
    """Check if a ticker is in the S&P MidCap 400."""
    return ticker in _SP400_SET


def is_in_universe(ticker: str) -> bool:
    """Check if a ticker is in the full universe."""
    return ticker in _ALL_SET


def universe_stats() -> dict:
    """Return summary statistics about the universe."""
    return {
        "sp500_count": len(SP500),
        "sp400_count": len(SP400),
        "etf_count": len(ETFS),
        "total_unique": len(_ALL_SET),
        "stocks_only": len(_SP500_SET | _SP400_SET),
    }


