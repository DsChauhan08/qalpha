ETFS: List[str] = ["SPY", "QQQ", "IWM", "VO", "MDY"]

# ============================================================================
# Precomputed sets for fast membership tests
# ============================================================================

_SP500_SET = frozenset(SP500)
_SP400_SET = frozenset(SP400)
_ALL_SET = frozenset(SP500 + SP400 + ETFS)


# ============================================================================
# Public API
# ============================================================================


def get_sp500() -> List[str]:
    """Return all S&P 500 constituents (502 tickers)."""
    return SP500.copy()


def get_sp400() -> List[str]:
    """Return all S&P MidCap 400 constituents (400 tickers)."""
    return SP400.copy()


def get_etfs() -> List[str]:
    """Return benchmark ETFs."""
    return ETFS.copy()


def get_full_universe() -> List[str]:
    """
    Return the full universe: S&P 500 + S&P MidCap 400 + ETFs.
    Deduplicated, sorted. ~907 tickers.
    """
    return sorted(_ALL_SET)


def get_stocks_only() -> List[str]:
    """
    Return all individual stocks (no ETFs).
    S&P 500 + S&P MidCap 400, deduplicated, sorted. ~902 tickers.
    """
    return sorted(_SP500_SET | _SP400_SET)


